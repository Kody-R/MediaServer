function populateGenres() {
    const genres = new Set(movies.map(movie => movie.genre));
    const genreSelect = document.getElementById('genreFilter');
    genres.forEach(genre => {
        genre.split(', ').forEach(g => {
            if (!Array.from(genreSelect.options).map(option => option.value).includes(g)) {
                const option = document.createElement('option');
                option.value = g;
                option.textContent = g;
                genreSelect.appendChild(option);
            }
        });
    });
}

function filterByGenre() {
    const selectedGenre = document.getElementById('genreFilter').value || 'All';
    const form = document.getElementById('movieForm');
    form.innerHTML = '';
    const filteredMovies = selectedGenre === 'All' ? movies : movies.filter(movie => movie.genre.includes(selectedGenre));
    filteredMovies.forEach(movie => createCheckboxForMovie(movie));
}

function createCheckboxForMovie(movie) {
    const displayName = `${movie.title} (${movie.year})`;
    const checkbox = document.createElement('input');
    checkbox.type = 'checkbox';
    checkbox.id = displayName;
    checkbox.name = 'movies';
    checkbox.value = JSON.stringify(movie);
    checkbox.checked = movie.selected;
    checkbox.onchange = () => updateSelection(movie, checkbox.checked);

    const label = document.createElement('label');
    label.htmlFor = displayName;
    label.textContent = displayName;

    const div = document.createElement('div');
    div.appendChild(checkbox);
    div.appendChild(label);
    document.getElementById('movieForm').appendChild(div);
}

function updateSelection(movie, isSelected) {
    const index = movies.findIndex(m => m.title === movie.title && m.year === movie.year);
    if (index !== -1) {
        movies[index].selected = isSelected;
    }
}

function selectAllVisible(select) {
    const checkboxes = document.querySelectorAll('#movieForm input[type="checkbox"]');
    checkboxes.forEach(checkbox => {
        checkbox.checked = select;
        const movie = JSON.parse(checkbox.value);
        updateSelection(movie, select);
    });
}

function downloadSelections() {
    const selectedMovies = movies.filter(movie => movie.selected);
    let selectedItems = selectedMovies.map(movie => `${movie.title} (${movie.year})`);

    // Calculate total size in MB
    let totalSizeMB = selectedMovies.reduce((sum, movie) => sum + movie.size, 0);

    // Convert total size from MB to GB or TB as needed
    let totalSizeGB = totalSizeMB / 1024;
    let sizeDisplay = totalSizeGB > 1024 ? `${(totalSizeGB / 1024).toFixed(2)} TB` : `${totalSizeGB.toFixed(2)} GB`;

    const summary = `Selected Movies:\n${selectedItems.join('\n')}\nTotal Size: ${sizeDisplay}`;
    const blob = new Blob([summary], { type: 'text/plain' });
    const href = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = href;
    link.download = 'SelectedMovies.txt';
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
}


function parseSize(size) {
    let value = parseFloat(size);
    if (size.toLowerCase().includes('gb')) {
        return value * 1024; // Convert GB to MB
    } else if (size.toLowerCase().includes('tb')) {
        return value * 1024 * 1024; // Convert TB to MB, though this might not be in your data as of now
    }
    return value; // Return as is if already in MB
}

